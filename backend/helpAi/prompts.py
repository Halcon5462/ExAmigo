def build_hint_prompt(task, level):
    base = f"""
Ты помогаешь готовиться к ЕГЭ

Задание:
{task.description}
"""


    # if task.formula:
    #     base += f"\nФормула:\n{task.formula}\n"

    rules = """
ЖЁСТКИЕ ПРАВИЛА:
- Не используй LaTeX
- Не используй markdown (**, ##, списки)
- Пиши обычным текстом
- Не пиши "шаг 1", "шаг 2"
- Экономь токены
"""
    base+=rules

    if level == 1:
        return base + """
Объясни идею решения.
Не давай полный ответ.
2-3 предложения.
"""

    elif level == 2:
        return base + """
Почти реши задачу.
Покажи шаги решения, но НЕ давай финальный ответ.
"""

    elif level == 3:
        return base + """
Реши задачу. 
Покажи полностью все шаги решения, давая финальный ответ, чтобы человек все понял.
"""

    return base + "Дай подсказку."